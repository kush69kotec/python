x_1 = float(input('Введите координату х1 первой точки'))
x_2 = float(input('Введите координату х2 первой точки'))
y_1 = float(input('Введите координату y1 второй точки'))
y_2 = float(input('Введите координату y2 второй точки'))

k = (y_1 - y_2) / (x_1 - x_2)
b = y_2 - k * x_2
print(f'Уравнение прямой: y = {k:.2f}x + {b:.2f}')

